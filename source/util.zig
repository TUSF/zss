const CSSUnit = @import("./properties.zig").CSSUnit;

pub const Offset = struct {
    x: CSSUnit,
    y: CSSUnit,

    const Self = @This();
    pub fn add(lhs: Self, rhs: Self) Self {
        return Self{ .x = lhs.x + rhs.x, .y = lhs.y + rhs.y };
    }
};
